export const UI_TEXT: Record<UiLanguage, Record<string, string>> = {
  zh: ko,
  en,
  ko,
};

export function formatUiText(template: string, params?: UiTextParams): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (match, key: string) => {
    const value = params[key];
    return value === undefined ? match : String(value);
  });
}

export function getUiText(language: UiLanguage, key: UiTextKey): string {
  return UI_TEXT[language][key] ?? UI_TEXT.ko[key] ?? UI_TEXT.en[key] ?? key;
}
